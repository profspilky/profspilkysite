class Command(BaseCommand):
    help = "Seed member organizations."

    def handle(self, *args, **options) -> None:
        from django.utils.text import slugify
        created = 0

        for idx, (name, _url) in enumerate(SECTORAL):
            slug = slugify(name, allow_unicode=False)[:280] or f"sectoral-{idx}"
            _, was_created = MemberOrganization.objects.get_or_create(
                slug=slug,
                defaults={
                    "name": name,
                    "org_type": OrgType.SECTORAL,
                    "order": idx,
                    "is_active": True,
                },
            )
            if was_created:
                created += 1

        for idx, (name, region) in enumerate(REGIONAL):
            slug = slugify(name, allow_unicode=False)[:280] or f"regional-{idx}"
            _, was_created = MemberOrganization.objects.get_or_create(
                slug=slug,
                defaults={
                    "name": name,
                    "org_type": OrgType.REGIONAL,
                    "region": region,
                    "order": idx,
                    "is_active": True,
                },
            )
            if was_created:
                created += 1

        self.stdout.write(self.style.SUCCESS(f"Done: {created} organizations created."))
